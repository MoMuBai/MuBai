public class MsgFactory {

        private static MsgService msgService;

        private static Object object = new Object();

        public static MsgService getMsgService() {
                if (null == msgService) {
                        synchronized (object) {
                                if (null == msgService) {
                                        msgService = new MsgClient().getApiService();
                                }
                        }
                }
                return msgService;
        }
}
